package classes;

/**
 *
 * @author Isabela Desiderio
 */
public class Produto {
    
    private int id;
    private String titulo;
    private String descricao;
    private int preco;
    private String itensCompoem;
    private String vendedor;
    private int qtdeDisponivel;
    private String endLoja;
    private int telefone;
    private String image;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public int getPreco() {
        return preco;
    }

    public void setPreco(int preco) {
        this.preco = preco;
    }

    public String getItensCompoem() {
        return itensCompoem;
    }

    public void setItensCompoem(String itensCompoem) {
        this.itensCompoem = itensCompoem;
    }

    public String getVendedor() {
        return vendedor;
    }

    public void setVendedor(String vendedor) {
        this.vendedor = vendedor;
    }

    public int getQtdeDisponivel() {
        return qtdeDisponivel;
    }

    public void setQtdeDisponivel(int qtdeDisponivel) {
        this.qtdeDisponivel = qtdeDisponivel;
    }

    public String getEndLoja() {
        return endLoja;
    }

    public void setEndLoja(String endLoja) {
        this.endLoja = endLoja;
    }

    public int getTelefone() {
        return telefone;
    }

    public void setTelefone(int telefone) {
        this.telefone = telefone;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
    
    
    
}
